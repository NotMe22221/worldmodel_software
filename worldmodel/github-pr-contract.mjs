export function githubRepositoryParts(fullName) {
  const match = /^([A-Za-z0-9_.-]+)\/([A-Za-z0-9_.-]+)$/.exec(fullName || "");
  if (!match) throw new Error("GitHub repository name is invalid");
  return { owner: match[1], repository: match[2] };
}

export function githubEvidencePath(proposalId) {
  if (!/^repair_[A-Za-z0-9_-]{1,100}$/.test(proposalId || ""))
    throw new Error("Repair proposal ID is invalid");
  return `.worldmodel/repairs/${proposalId}.json`;
}

export function githubDraftBody(packet) {
  const observed = packet.scenario.evidenceKind === "observed";
  const evidenceNotice = observed
    ? "> OBSERVED EVIDENCE — Metrics were captured from an identified virtual test environment."
    : "> ⚠️ MODELED EVIDENCE — Metrics are deterministic projections and were not observed in a customer virtual test environment.";
  return `## WorldModel repair candidate\n\n${evidenceNotice}\n\nThis draft contains an ownership-validated repository evidence packet for **${packet.repair.title}**. Human review and observed execution remain required before merge.\n\n### Identical replay\n- Scenario: ${packet.scenario.name}\n- Fingerprint: \`${packet.scenario.fingerprint}\`\n- Evidence kind: ${packet.scenario.evidenceKind || "modeled"}\n- Resilience: ${packet.scenario.beforeScore} → ${packet.scenario.afterScore}\n- Verified: ${packet.scenario.verifiedAt}\n\n### Review\n- Approved by: ${packet.review.approvedBy}\n- Approved at: ${packet.review.approvedAt}\n- Evidence: ${packet.review.decisionNote}\n\n### Residual risks\n${packet.repair.residualRisks.map((risk) => `- ${risk}`).join("\n")}\n\nGenerated by WorldModel for Software. This PR is intentionally a draft.`;
}
