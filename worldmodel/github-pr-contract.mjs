export function githubRepositoryParts(fullName) {
  const match = /^([A-Za-z0-9_.-]+)\/([A-Za-z0-9_.-]+)$/.exec(fullName || "");
  if (!match) throw new Error("GitHub repository name is invalid");
  return { owner: match[1], repository: match[2] };
}

export function githubEvidencePath(proposalId) {
  if (!/^repair_[A-Za-z0-9_-]{1,100}$/.test(proposalId || ""))
    throw new Error("Repair proposal ID is invalid");
  return `.worldmodel/repairs/${proposalId}.json`;
}

export function githubDraftBody(packet) {
  return `## WorldModel verified repair\n\nThis draft contains a tenant-owned evidence packet for **${packet.repair.title}**. Human review remains required before merge.\n\n### Identical replay\n- Scenario: ${packet.scenario.name}\n- Fingerprint: \`${packet.scenario.fingerprint}\`\n- Resilience: ${packet.scenario.beforeScore} → ${packet.scenario.afterScore}\n- Verified: ${packet.scenario.verifiedAt}\n\n### Review\n- Approved by: ${packet.review.approvedBy}\n- Approved at: ${packet.review.approvedAt}\n- Evidence: ${packet.review.decisionNote}\n\n### Residual risks\n${packet.repair.residualRisks.map((risk) => `- ${risk}`).join("\n")}\n\nGenerated by WorldModel for Software. This PR is intentionally a draft.`;
}
